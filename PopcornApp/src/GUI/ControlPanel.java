    private void orderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderActionPerformed
        // TODO add your handling code here:
         String[] products = new String[3];
    int[] quantities = new int[3];
    int index = 0;

    if (salt.isSelected()) {
        products[index] = "Salt";
        quantities[index] = Integer.parseInt(saltnumber.getText());
        index++;
    }
    if (cheese.isSelected()) {
        products[index] = "Cheese";
        quantities[index] = Integer.parseInt(cheeseorder.getText());
        index++;
    }
    if (caramel.isSelected()) {
        products[index] = "Caramel";
        quantities[index] = Integer.parseInt(caramelorder.getText());
        index++;
    }

    if (index > 0) {
        String[] finalProducts = new String[index];
        int[] finalQuantities = new int[index];

    for(int i = 0; i < index; i++) {
    finalProducts[i] = products[i];
    }

    for (int i = 0; i < index; i++) {
    finalQuantities[i] = quantities[i];
    }


        if (!orderQueue.isFull()) {
            orderQueue.insert(index, finalProducts, finalQuantities);
            javax.swing.JOptionPane.showMessageDialog(this, "order placed successfully!");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "queue is full, Please wait.");
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "no items selected.");
    }
    }//GEN-LAST:event_orderActionPerformed

