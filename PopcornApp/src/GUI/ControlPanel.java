    private void viewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewActionPerformed
        // TODO add your handling code here:
       if (!orderQueue.isEmpty()) {
        Order nextOrder = orderQueue.remove();
        
        
        System.out.println("Order being viewed:");
        String[] products = nextOrder.getProducts();
        int[] quantities = nextOrder.getQuantity();

        for (int i = 0; i < products.length; i++) {
            System.out.println(products[i] + ": " + quantities[i]);
        }
        
        
        StringBuilder orderDetails = new StringBuilder();
        for (int i = 0; i < products.length; i++) {
            orderDetails.append(products[i]).append(": ").append(quantities[i]).append("\n");
        }
        
        display.setText(orderDetails.toString());
    } else {
        display.setText("No orders in the queue.");
    }
    }//GEN-LAST:event_viewActionPerformed

